public class AppointmentInfoModel {
	private int appointment_id;
	private String appointment_name;
	private String appointment_age;
	private String appointment_doctor;
	private String appointment_department;
	private String appointment_date;
	private String appointment_time;
	private String appointment_status;
	public int getAppointment_id() {
		return appointment_id;
	}
	public void setAppointment_id(int appointment_id) {
		this.appointment_id = appointment_id;
	}
	public String getAppointment_name() {
		return appointment_name;
	}
	public void setAppointment_name(String appointment_name) {
		this.appointment_name = appointment_name;
	}
	public String getAppointment_age() {
		return appointment_age;
	}
	public void setAppointment_age(String appointment_age) {
		this.appointment_age = appointment_age;
	}
	public String getAppointment_doctor() {
		return appointment_doctor;
	}
	public void setAppointment_doctor(String appointment_doctor) {
		this.appointment_doctor = appointment_doctor;
	}
	public String getAppointment_department() {
		return appointment_department;
	}
	public void setAppointment_department(String appointment_department) {
		this.appointment_department = appointment_department;
	}
	public String getAppointment_date() {
		return appointment_date;
	}
	public void setAppointment_date(String appointment_date) {
		this.appointment_date = appointment_date;
	}
	public String getAppointment_time() {
		return appointment_time;
	}
	public void setAppointment_time(String appointment_time) {
		this.appointment_time = appointment_time;
	}
	public String getAppointment_status() {
		return appointment_status;
	}
	public void setAppointment_status(String appointment_status) {
		this.appointment_status = appointment_status;
	}
	
}
